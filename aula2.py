print(1234)

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"') ## um numero, string

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'") ## um numero, string

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")