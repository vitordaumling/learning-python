"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Luiz Otávio')
## Aspas duplas aparecem dentro de aspas simples
print(1, 'Luiz "Otávio"') ## um numero, string

# Aspas duplas
print("Luiz Otávio")
## Aspas simples aparecem dentro de aspas duplas
print(2, "Luiz 'Otávio'") ## um numero, string

# Escape
print("Luiz \"Otávio\"")

# r (mostra os caracteres de escape)
print(r"Luiz \"Otávio\"")