# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1'))) # Converte int e mostra o tipo int convertido
print(type(float('1') + 1)) # type float
print(bool(' ')) ## Converte em boom
print(str(11) + 'b') # Convertendo e concatenando string